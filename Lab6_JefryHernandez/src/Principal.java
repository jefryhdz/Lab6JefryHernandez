        Lugar c = (Lugar)cb_list.getSelectedItem();
        ArrayList <Carretera>cd = c.getEntradas();
        for (Carretera xc : cd) {
            if (xc.getDistancia()<=50) {
                model.addElement(xc);
            }
        }
